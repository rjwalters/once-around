/// The golden regression test. Uses exact f64 equality (see contract at top of file).
#[test]
fn test_golden_body_positions() {
    let time = epoch();

    // --- Celestial bodies (all 9) ---
    let bodies = compute_all_body_positions_full(&time);
    for (i, expected) in GOLDEN_BODIES.iter().enumerate() {
        let b = &bodies[i];
        assert_eq!(b.direction.x, expected.0, "body[{i}] direction.x");
        assert_eq!(b.direction.y, expected.1, "body[{i}] direction.y");
        assert_eq!(b.direction.z, expected.2, "body[{i}] direction.z");
        assert_eq!(b.distance_km, expected.3, "body[{i}] distance_km");
    }

    // --- Planetary moons ---
    let moons = compute_all_planetary_moon_positions(&time);
    let ganymede = &moons[GANYMEDE_IDX];
    assert_eq!(ganymede.direction.x, GOLDEN_GANYMEDE.0, "Ganymede direction.x");
    assert_eq!(ganymede.direction.y, GOLDEN_GANYMEDE.1, "Ganymede direction.y");
    assert_eq!(ganymede.direction.z, GOLDEN_GANYMEDE.2, "Ganymede direction.z");
    assert_eq!(ganymede.distance_km, GOLDEN_GANYMEDE.3, "Ganymede distance_km");

    let titan = &moons[TITAN_IDX];
    assert_eq!(titan.direction.x, GOLDEN_TITAN.0, "Titan direction.x");
    assert_eq!(titan.direction.y, GOLDEN_TITAN.1, "Titan direction.y");
    assert_eq!(titan.direction.z, GOLDEN_TITAN.2, "Titan direction.z");
    assert_eq!(titan.distance_km, GOLDEN_TITAN.3, "Titan distance_km");

    // --- Minor bodies ---
    let minor = compute_all_minor_body_positions(&time);
    let pluto = &minor[PLUTO_IDX];
    assert_eq!(pluto.direction.x, GOLDEN_PLUTO.0, "Pluto direction.x");
    assert_eq!(pluto.direction.y, GOLDEN_PLUTO.1, "Pluto direction.y");
    assert_eq!(pluto.direction.z, GOLDEN_PLUTO.2, "Pluto direction.z");
    assert_eq!(pluto.distance_km, GOLDEN_PLUTO.3, "Pluto distance_km");
    assert_eq!(pluto.helio_distance_km, GOLDEN_PLUTO.4, "Pluto helio_distance_km");

    let ceres = &minor[CERES_IDX];
    assert_eq!(ceres.direction.x, GOLDEN_CERES.0, "Ceres direction.x");
    assert_eq!(ceres.direction.y, GOLDEN_CERES.1, "Ceres direction.y");
    assert_eq!(ceres.direction.z, GOLDEN_CERES.2, "Ceres direction.z");
    assert_eq!(ceres.distance_km, GOLDEN_CERES.3, "Ceres distance_km");
    assert_eq!(ceres.helio_distance_km, GOLDEN_CERES.4, "Ceres helio_distance_km");

    // --- Comets ---
    let comets = compute_all_comet_positions(&time);
    let halley = &comets[HALLEY_IDX];
    assert_eq!(halley.direction.x, GOLDEN_HALLEY.0, "Halley direction.x");
    assert_eq!(halley.direction.y, GOLDEN_HALLEY.1, "Halley direction.y");
    assert_eq!(halley.direction.z, GOLDEN_HALLEY.2, "Halley direction.z");
    assert_eq!(halley.distance_km, GOLDEN_HALLEY.3, "Halley distance_km");
    assert_eq!(halley.helio_distance_km, GOLDEN_HALLEY.4, "Halley helio_distance_km");

    let neowise = &comets[NEOWISE_IDX];
    assert_eq!(neowise.direction.x, GOLDEN_NEOWISE.0, "NEOWISE direction.x");
    assert_eq!(neowise.direction.y, GOLDEN_NEOWISE.1, "NEOWISE direction.y");
    assert_eq!(neowise.direction.z, GOLDEN_NEOWISE.2, "NEOWISE direction.z");
    assert_eq!(neowise.distance_km, GOLDEN_NEOWISE.3, "NEOWISE distance_km");
    assert_eq!(neowise.helio_distance_km, GOLDEN_NEOWISE.4, "NEOWISE helio_distance_km");
}

/// Generator for the golden constants above. Ignored by default; run on demand to
/// regenerate the literals after a deliberate algorithm change:
///
/// ```text
/// cargo test -p sky_engine_core --test golden_positions \
///     generate_golden_constants -- --ignored --nocapture
/// ```
///
/// Copy the printed literals verbatim into the `GOLDEN_*` constants. Uses `{:?}`
/// formatting, which emits the shortest decimal that round-trips to the exact same f64.
#[test]
#[ignore]
fn generate_golden_constants() {
    let time = epoch();
    let bodies = compute_all_body_positions_full(&time);
    let moons = compute_all_planetary_moon_positions(&time);
    let minor = compute_all_minor_body_positions(&time);
    let comets = compute_all_comet_positions(&time);

    println!("\n===== BEGIN GOLDEN CONSTANTS (paste into golden_positions.rs) =====\n");

    println!("const GOLDEN_BODIES: [(f64, f64, f64, f64); 9] = [");
    let names = [
        "Sun", "Moon", "Mercury", "Venus", "Mars", "Jupiter", "Saturn", "Uranus", "Neptune",
    ];
    for (i, b) in bodies.iter().enumerate() {
        println!(
            "    ({:?}, {:?}, {:?}, {:?}), // {}",
            b.direction.x, b.direction.y, b.direction.z, b.distance_km, names[i]
        );
    }
    println!("];\n");

    let g = &moons[GANYMEDE_IDX];
    println!(
        "const GOLDEN_GANYMEDE: (f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?});",
        g.direction.x, g.direction.y, g.direction.z, g.distance_km
    );
    let t = &moons[TITAN_IDX];
    println!(
        "const GOLDEN_TITAN: (f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?});\n",
        t.direction.x, t.direction.y, t.direction.z, t.distance_km
    );

    let p = &minor[PLUTO_IDX];
    println!(
        "const GOLDEN_PLUTO: (f64, f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?}, {:?});",
        p.direction.x, p.direction.y, p.direction.z, p.distance_km, p.helio_distance_km
    );
    let c = &minor[CERES_IDX];
    println!(
        "const GOLDEN_CERES: (f64, f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?}, {:?});\n",
        c.direction.x, c.direction.y, c.direction.z, c.distance_km, c.helio_distance_km
    );

    let h = &comets[HALLEY_IDX];
    println!(
        "const GOLDEN_HALLEY: (f64, f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?}, {:?});",
        h.direction.x, h.direction.y, h.direction.z, h.distance_km, h.helio_distance_km
    );
    let n = &comets[NEOWISE_IDX];
    println!(
        "const GOLDEN_NEOWISE: (f64, f64, f64, f64, f64) = ({:?}, {:?}, {:?}, {:?}, {:?});\n",
        n.direction.x, n.direction.y, n.direction.z, n.distance_km, n.helio_distance_km
    );

    println!("===== END GOLDEN CONSTANTS =====\n");
}
