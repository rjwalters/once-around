//! Golden-value regression test for body / moon / minor-body / comet positions.
//!
//! # Why this exists
//!
//! After issue #3 the standalone `compute_all_*` functions became thin wrappers that
//! build a [`TimeContext`] internally and delegate to the `*_with_ctx` variant. The
//! existing `shared_ctx_bit_identical_to_independent_path` equivalence test therefore
//! compares new-vs-new (an externally supplied context against an internally built one)
//! rather than new-vs-pre-refactor code. That test cannot, on its own, catch a future
//! regression in the shared `TimeContext` derivation (nutation series, obliquity, Earth
//! VSOP87 vector, call ordering).
//!
//! This file pins the full-precision f64 outputs for a curated set of targets at a fixed
//! epoch to literal constants captured from the current implementation. It is an
//! epoch-anchored old-vs-new guard that is independent of the refactored call graph.
//!
//! # Exact-equality contract (read before touching the constants)
//!
//! These assertions use exact f64 equality (`==`, no tolerance). This is intentional:
//! bit-identity is the entire point. Golden tests of this form break on ANY legitimate
//! algorithm change. That is the intended behavior — any change to the `TimeContext`
//! derivation, nutation series, VSOP87 call order, or obliquity computation forces
//! conscious regeneration of these constants. When a change is deliberate, re-run the
//! generator and update the literals below:
//!
//! ```text
//! cargo test -p sky_engine_core --test golden_positions \
//!     generate_golden_constants -- --ignored --nocapture
//! ```
//!
//! Do NOT add floating-point tolerances here. A failure means either an unintended
//! regression (investigate) or a deliberate algorithm change (regenerate consciously).

use sky_engine_core::planets::compute_all_body_positions_full;
use sky_engine_core::{
    compute_all_comet_positions, compute_all_minor_body_positions,
    compute_all_planetary_moon_positions, SkyTime,
};

/// Fixed epoch: 2026-07-06 00:00:00 UTC (JD ~= 2461227.5).
///
/// Built via `from_utc` deliberately. Note: the `TEST_JDS` array in `time_context.rs`
/// carries a comment that conflates JD `2461041.5` with "2026-07-06"; that JD is
/// actually 2026-01-01. Do not anchor this test on that JD — use `from_utc` so the
/// epoch is unambiguous.
fn epoch() -> SkyTime {
    SkyTime::from_utc(2026, 7, 6, 0, 0, 0.0)
}

